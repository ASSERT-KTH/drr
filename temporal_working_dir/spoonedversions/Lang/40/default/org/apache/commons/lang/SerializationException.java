

package org.apache.commons.lang;


public class SerializationException extends java.lang.RuntimeException {
	private static final long serialVersionUID = 4029025366392702726L;

	public SerializationException() {
		super();
	}

	public SerializationException(java.lang.String msg) {
		super(msg);
	}

	public SerializationException(java.lang.Throwable cause) {
		super(cause);
	}

	public SerializationException(java.lang.String msg, java.lang.Throwable cause) {
		super(msg, cause);
	}
}

