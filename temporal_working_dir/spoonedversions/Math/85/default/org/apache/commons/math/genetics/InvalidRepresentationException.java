

package org.apache.commons.math.genetics;


public class InvalidRepresentationException extends java.lang.Exception {
	private static final long serialVersionUID = 1L;

	public InvalidRepresentationException() {
		super();
	}

	public InvalidRepresentationException(java.lang.String arg0) {
		super(arg0);
	}

	public InvalidRepresentationException(java.lang.Throwable arg0) {
		super(arg0);
	}

	public InvalidRepresentationException(java.lang.String arg0, java.lang.Throwable arg1) {
		super(arg0, arg1);
	}
}

