

package org.apache.commons.math;


public class MathConfigurationException extends org.apache.commons.math.MathException implements java.io.Serializable {
	private static final long serialVersionUID = -4056541384141349722L;

	public MathConfigurationException() {
		super();
	}

	public MathConfigurationException(java.lang.String message) {
		super(message);
	}

	public MathConfigurationException(java.lang.String pattern, java.lang.Object[] arguments) {
		super(pattern, arguments);
	}

	public MathConfigurationException(java.lang.String message, java.lang.Throwable cause) {
		super(message, cause);
	}

	public MathConfigurationException(java.lang.Throwable cause) {
		super(cause);
	}

	public MathConfigurationException(java.lang.String pattern, java.lang.Object[] arguments, java.lang.Throwable cause) {
		super(pattern, arguments, cause);
	}
}

